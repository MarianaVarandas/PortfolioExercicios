package Json.titulo;

public class Veiculo {
    private String marca;
    private String modelo;
    private int ano;
    private int quilometrosRodados;

    public Veiculo(String marca, String modelo, int ano, int quilometrosRodados) {
        this.marca = marca;
        this.modelo = modelo;
        this.ano = ano;
        this.quilometrosRodados = quilometrosRodados;
    }
}
